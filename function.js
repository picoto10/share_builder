var contenedor = document.getElementById("container");

function mostrar(tag){
  var count = 0;
  contenedor.innerHTML = "";
  if(tag == 'todas'){
    var arreglo = aleatorias();
    for (var i = 0; i < arreglo.length; i++) {
      count++;
      var pos = arreglo[i];
      construir(estrategias[pos]);
    }
  }else{
    for (var i = 0; i < estrategias.length; i++) {
      if (estrategias[i].tag.indexOf(tag) != -1) {
        count++;
        construir(estrategias[i]);
        console.log(estrategias[i].cargar());
      }
    }
  }
  if (count == 0) {
    contenedor.innerHTML = '<h1>Opps... No existen estrategias planteadas para esta habilidad</h1>';
  }
}

function construir(estrategia){
  contenedor.innerHTML += `
  <div id="card" class="card">
  <div id="card-header" class="card-header">
    `+acortar(estrategia.nombre)+`
  </div>
  <div id="card-content" class="card-content">
  <span class="`+codigos(estrategia.tipo)+` etiqueta">`+estrategia.componente+`</span>
  </div>
  <div id="card-footer" class="card-footer">
  <a href="#" id="card-button" class="card-button" onclick = "abrirEstrategia('`+estrategia.url+`')">Ver estrategia</a>
  </div>
  </div>
  `;
}

function aleatorias(){
  var arreglo = new Array(9);
  var count = 0;
  while(count < arreglo.length){
    const random = Math.floor(Math.random()*63);
    if(arreglo.indexOf(random) == -1){
      arreglo[count] = random;
      count++;
    }
  }
  return arreglo;
}

function codigos(componente){
  switch (componente) {
    case 1:
      return "tipo1";
    case 2:
      return "tipo2";
    case 3:
      return "tipo3";
    case 4:
      return "tipo4";
    default:
      return "tipo1";
  }
}

function acortar(palabra){
  if(palabra.length >= 32){
    return palabra.substring(0,45)+"...";
  }else{
    return palabra+'<br>&nbsp;';
  }
}

function abrirEstrategia(url){
  window.open(url,"_blank");
}

